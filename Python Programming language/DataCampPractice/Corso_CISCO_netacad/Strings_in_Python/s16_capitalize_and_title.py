# The
# title() method
# performs a somewhat similar function - it changes EVERY word's first letter to upper-case,
# turning all other ones to lower-case.

# Demonstrating the title() method
print("I know that I know nothing. Part 1.".title())

s1 = "I know that I know nothing. Part 1."
print("Con title:", s1.title())
print("Con capitalize:", s1.capitalize())
